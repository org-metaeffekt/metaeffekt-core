package org.metaeffekt.core.inventory.processor.model;

public class DependencyData implements Comparable<DependencyData> {

    private String name;

    private String artifactId;

    private String groupId;

    private String version;

    private String classifier;

    private String scope;

    private boolean optional;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getArtifactId() {
        return artifactId;
    }

    public void setArtifactId(String artifactId) {
        this.artifactId = artifactId;
    }

    public String getGroupId() {
        return groupId;
    }

    public void setGroupId(String groupId) {
        this.groupId = groupId;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getClassifier() {
        return classifier;
    }

    public void setClassifier(String classifier) {
        this.classifier = classifier;
    }

    public String getScope() {
        return scope;
    }

    public void setScope(String scope) {
        this.scope = scope;
    }

    public boolean isOptional() {
        return optional;
    }

    public void setOptional(boolean optional) {
        this.optional = optional;
    }

    @Override
    public int compareTo(DependencyData other) {
        if (this.artifactId != null && other.getArtifactId() != null) {
            return (this.artifactId.compareTo(other.getArtifactId()));
        }
        return 1;
    }

}
