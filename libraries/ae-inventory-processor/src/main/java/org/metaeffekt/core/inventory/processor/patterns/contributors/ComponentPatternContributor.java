package org.metaeffekt.core.inventory.processor.patterns.contributors;

import org.metaeffekt.core.inventory.processor.model.ComponentPatternData;
import org.metaeffekt.core.util.FileUtils;

import java.io.File;
import java.util.Collections;
import java.util.List;
import java.util.Optional;

public abstract class ComponentPatternContributor {

    /**
     * Checks whether the contributor can work with the given file.
     * @param pathInContext a partial path for running the check on; might not be a real path
     *
     * @return returns whether this contributor can do something with the given file
     */
    public abstract boolean applies(String pathInContext);

    /**
     * Runs the contributor and tries to create component patterns from the given baseDir and anchor.
     *
     * @param baseDir                the directory that other files are contained in
     * @param virtualRootPath        the virtual root path based on the context
     * @param relativeAnchorFilePath anchor relative to basedir
     * @param checksum               a checksum of the file that is being used as an anchor
     * @param context {@link EvaluationContext} instance for contributors tracking different anchors.
     *
     * @return component patterns generated by the rules of the contribute method
     */
    public List<ComponentPatternData> contribute(File baseDir, String virtualRootPath, String relativeAnchorFilePath, String checksum, EvaluationContext context) {
        return contribute(baseDir, virtualRootPath, relativeAnchorFilePath, checksum);
    }

    /**
     * Runs the contributor and tries to create component patterns from the given baseDir and anchor.
     *
     * @param baseDir                the directory that other files are contained in
     * @param virtualRootPath        the virtual root path based on the context
     * @param relativeAnchorFilePath anchor relative to basedir
     * @param checksum               a checksum of the file that is being used as an anchor
     * @return component patterns generated by the rules of the contribute method
     */
    public List<ComponentPatternData> contribute(File baseDir, String virtualRootPath, String relativeAnchorFilePath, String checksum) {
        return Collections.emptyList();
    }

    /**
     * Get a list of suffixes that this contributor may be able to use as an anchor.<br>
     * Note that this is not limited to filename extensions.
     *
     * @return a list of supported file suffixes
     */
    public abstract List<String> getSuffixes();

    /**
     * Get the execution phase of this contributor. The lower the number, the earlier it will be executed.
     * @return the execution phase
     */
    public abstract int getExecutionPhase();

    /**
     * Tries to resolve the virtualRootPath of the given relativeAnchorPath. The relativeAnchorPath is relative to
     * baseDir. The virtualRootPath may be in between baseDir and the partent of the anchor.
     *
     * @param baseDir The baseDir.
     * @param relativeAnchorPath The relative path to the anchor file from the baseDir.
     * @param validPathFragments The possible paths, where the root is the virtualRootPath.
     *
     * @return The relative path to the virtualRootPath based on baseDir.
     */
    protected String modulateVirtualRootPath(File baseDir, String relativeAnchorPath, List<String> validPathFragments) {
        File dir = new File(baseDir, relativeAnchorPath);
        String path = dir.getName();
        dir = dir.getParentFile();

        while (true) {
            final String currentPath = path;
            final Optional<String> matchingPathFragment = validPathFragments.stream().filter(l -> currentPath.startsWith(l)).findFirst();
            if (matchingPathFragment.isPresent()) break;

            path = dir.getName() + "/" + path;
            dir = dir.getParentFile();
            if (dir.equals(baseDir)) {
                break;
            }
        }

        return FileUtils.asRelativePath(baseDir, dir);
    }
}
